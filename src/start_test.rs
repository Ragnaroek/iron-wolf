        && a.class == b.class
        && a.flags == b.flags
        && a.distance == b.distance
        && a.dir == b.dir
        && a.x == b.x
        && a.y == b.y
        && a.tilex == b.tilex
        && a.tiley == b.tiley
        && a.view_x == b.view_x
        && a.view_height == b.view_height
        && a.trans_x == b.trans_y
        && a.trans_y == b.trans_y
        && a.area_number == b.area_number
