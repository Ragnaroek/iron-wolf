// Read a original W3D savegame and writes it back. Should result in almost
// the same save game files (there are currently differences that are WiP to achieve
// a full load of savegames in IW)
#[test]
fn test_do_load_and_save_save0() {
    let episode = 0;
    let map_on = 1;
    let mut data_path = PathBuf::new();
    data_path.push("./testdata/shareware_data");

    let loader = DiskLoader {
        variant: &assets::W3D1,
        data_path,
        patch_path: None,
    };

    let (_, rdr, assets) = start_test_iw(&loader);

    let mut game_state = new_game_state();
    game_state.difficulty = Difficulty::Baby;
    game_state.episode = episode; // set this here so that correct level is set up in 'setup_game_level' call
    game_state.map_on = map_on;
    let mut level_state_init = setup_game_level(&mut game_state, &assets).expect("level state");

    let mut level_state = setup_game_level(&mut game_state, &assets).expect("level state");

    let mut iw_config = default_iw_config().expect("config");
    iw_config.options.fast_loading = true;

    // reset actor data, to modify them and check that they are read correctly in.
    // otherwise level_state_init.actors == level_state.actors, even if nothing is read and copied.
    for actor in &mut level_state.actors {
        reset_partial_obj_type(actor);
    }

    // change some default values to check if it is overwritten
    game_state.best_weapon = WeaponType::Knife;
    game_state.weapon = None;
    game_state.chosen_weapon = WeaponType::Knife;
    game_state.push_wall_state = 666;
    game_state.loaded_game = true;
    let checksum_passed = do_load(
        &iw_config,
        &mut level_state,
        &mut game_state,
        &rdr,
        &assets,
        &loader,
        0,
        0,
        0,
    );
    assert!(checksum_passed);
    // check game_state
    check_loaded_save_0(
        &level_state,
        &game_state,
        &mut level_state_init,
        episode,
        map_on,
    );

    // save the state as save9 and check if it is the ~same as save0 (there are some differences in an iw
    // save saved game)
    save_the_game(
        &iw_config,
        &level_state,
        &game_state,
        &rdr,
        &loader,
        9,
        "e1m2",
        0,
        0,
    );
    check_written_save_0(&loader);

    // load save9 again, it should result in the same game state as the load of save0
    let checksum_passed = do_load(
        &iw_config,
        &mut level_state,
        &mut game_state,
        &rdr,
        &assets,
        &loader,
        9, /*only difference to load before*/
        0,
        0,
    );
    assert!(checksum_passed);
    // check game_state again after save9 load
    check_loaded_save_0(
        &level_state,
        &game_state,
        &mut level_state_init,
        episode,
        map_on,
    );
}

fn check_loaded_save_0(
    level_state: &LevelState,
    game_state: &GameState,
    level_state_init: &mut LevelState,
    episode: usize,
    map_on: usize,
) {
    assert_eq!(game_state.difficulty, Difficulty::Baby);
    assert_eq!(game_state.map_on, map_on);
    assert_eq!(game_state.old_score, 3700);
    assert_eq!(game_state.score, 3700);
    assert_eq!(game_state.next_extra, 40000);
    assert_eq!(game_state.lives, 3);
    assert_eq!(game_state.health, 100);
    assert_eq!(game_state.ammo, 46);
    assert_eq!(game_state.keys, 0);
    assert_eq!(game_state.best_weapon, WeaponType::MachineGun);
    assert_eq!(game_state.weapon, Some(WeaponType::MachineGun));
    assert_eq!(game_state.chosen_weapon, WeaponType::MachineGun);
    assert_eq!(game_state.face_frame, 0);
    assert_eq!(game_state.attack_frame, 0);
    assert_eq!(game_state.attack_count, 65535);
    assert_eq!(game_state.weapon_frame, 0);
    assert_eq!(game_state.episode, episode);
    assert_eq!(game_state.secret_count, 0);
    assert_eq!(game_state.treasure_count, 0);
    assert_eq!(game_state.kill_count, 0);
    assert_eq!(game_state.secret_total, 4);
    assert_eq!(game_state.treasure_total, 62);
    assert_eq!(game_state.kill_total, 40);
    assert_eq!(game_state.time_count, 47);
    assert_eq!(game_state.kill_x, 0);
    assert_eq!(game_state.kill_y, 0);
    assert_eq!(game_state.victory_flag, false);
    // push wall state
    assert_eq!(game_state.push_wall_state, 0);
    assert_eq!(game_state.push_wall_dir, Dir::South);
    assert_eq!(game_state.push_wall_x, 18);
    assert_eq!(game_state.push_wall_y, 50);
    assert_eq!(game_state.push_wall_pos, 63);

    //check level ratios
    assert_eq!(game_state.level_ratios.len(), 8);
    for i in 0..game_state.level_ratios.len() {
        let ratio = &game_state.level_ratios[i];
        if i == 0 {
            assert_eq!(ratio.kill, 90);
            assert_eq!(ratio.secret, 40);
            assert_eq!(ratio.treasure, 21);
            assert_eq!(ratio.time, 253);
        } else {
            assert_eq!(ratio.kill, 0);
            assert_eq!(ratio.secret, 0);
            assert_eq!(ratio.treasure, 0);
            assert_eq!(ratio.time, 0);
        }
    }

    // check LevelState
    // The test savegame is from the start of E1M1, so no tile_map data have been modified
    // and it should match the tile_map from the initial state.
    for x in 0..MAP_SIZE {
        for y in 0..MAP_SIZE {
            assert_eq!(
                level_state.level.tile_map[x][y], level_state_init.level.tile_map[x][y],
                "diff tile_map[{}][{}]",
                x, y
            );
        }
    }

    let w3d_actor_at = w3d_actor_at_save0(&level_state_init.actor_at);
    for x in 0..MAP_SIZE {
        for y in 0..MAP_SIZE {
            assert_eq!(
                level_state.actor_at[x][y], w3d_actor_at[x][y],
                "diff actor_at[{}][{}]",
                x, y
            );
        }
    }

    let w3d_area_connect = w3d_area_connect_save0();
    for x in 0..NUM_AREAS {
        for y in 0..NUM_AREAS {
            assert_eq!(
                level_state.area_connect[x][y], w3d_area_connect[x][y],
                "diff area_connect[{}][{}]",
                x, y
            );
        }
    }

    let w3d_area_by_player = w3d_area_by_player_save0();
    for i in 0..NUM_AREAS {
        assert_eq!(
            level_state.area_by_player[i], w3d_area_by_player[i],
            "diff area_by_player[{}]",
            i
        )
    }

    // modified values here on the expected player have been cross checked with W3D
    level_state_init.mut_player().dir = DirType::East;
    level_state_init.mut_player().x = 1081344;
    level_state_init.mut_player().y = 4030464;
    level_state_init.mut_player().tilex = 16;
    level_state_init.mut_player().tiley = 61;
    level_state_init.mut_player().area_number = 12;

    let mut want_player = level_state_init.player().clone();
    want_player.state = None; // state is currently not read back correctly from a W3D savegame
    assert_eq!(level_state.player(), &want_player);

    assert_eq!(level_state.actors.len(), level_state_init.actors.len());
    let w3d_actors = w3d_actors_save0();
    for i in 0..level_state.actors.len() {
        assert!(
            partial_check_obj(&level_state.actors[i], &w3d_actors[i]),
            "actors[{}] mismatch",
            i
        );
    }

    assert_eq!(level_state.statics.len(), level_state_init.statics.len());
    let w3d_statics = w3d_statics_save0();
    for i in 0..level_state.statics.len() {
        assert_eq!(
            level_state.statics[i], w3d_statics[i],
            "statics[{}] mismatch",
            i
        );
    }

    let w3d_doors = w3d_door_types_save0();
    for i in 0..level_state.doors.len() {
        assert_eq!(level_state.doors[i], w3d_doors[i]);
    }
}

fn check_written_save_0(loader: &DiskLoader) {
    let orig_save = loader.load_save_game(0).expect("orig save game loaded");
    let test_save = loader.load_save_game(9).expect("test save game loaded");

    assert_eq!(orig_save.len(), test_save.len());
    for i in 0..4274 {
        if i >= 5 && i <= 32 {
            // header beyond the save game name contains garbage, but iw always writes 0s
            assert_eq!(test_save[i], 0);
        } else {
            assert_eq!(
                orig_save[i], test_save[i],
                "bytes[{}] differ  {:x} (orig) vs {:x} (test)",
                i, orig_save[i], test_save[i]
            );
        }
    }
    // at_val check
    for i in (4274..12466).step_by(2) {
        let got_val = u16::from_le_bytes(test_save[i..i + 2].try_into().expect("le bytes"));
        let want_val = u16::from_le_bytes(orig_save[i..i + 2].try_into().expect("le bytes"));
        if want_val > 255 {
            assert!(got_val > 255, "at_val obj ref differ");
        } else {
            assert_eq!(got_val, want_val, "at_val differ")
        }
    }

    let obj_offset_start = 13909;
    let obj_offset_end = 16429;

    for i in 12466..obj_offset_start {
        assert_eq!(
            orig_save[i], test_save[i],
            "bytes[{}] differ {:x} (orig) vs {:x} (test)",
            i, orig_save[i], test_save[i]
        );
    }

    let mut next_obj_start = obj_offset_start;
    for i in obj_offset_start..obj_offset_end {
        if i == next_obj_start + 6 || i == next_obj_start + 7 {
            // state ptr wanted diff
            assert_eq!(test_save[i], 0, "state pointer not nulled");
        } else if i >= next_obj_start + OBJ_TYPE_LEN - 4 && i <= next_obj_start + OBJ_TYPE_LEN - 1 {
            //next,prev pointer wanted diff
            assert_eq!(test_save[i], 0, "next, prev pointer not nulled")
        } else if i >= (obj_offset_end - OBJ_TYPE_LEN) + 2 && i <= obj_offset_end {
            // the original save game contains garbage data in the nullobj. iw zeros it out.
            // only the 'active' field is expected to be as in the original savegame
            assert_eq!(test_save[i], 0, "zero object not nulled, offset = {}", i);
        } else {
            assert_eq!(
                orig_save[i], test_save[i],
                "obj_type bytes[{}] differ {:x} (orig) vs {:x} (test)",
                i, orig_save[i], test_save[i]
            );
        }

        if i == next_obj_start + (OBJ_TYPE_LEN - 1) {
            next_obj_start += OBJ_TYPE_LEN;
        }
    }

    assert_eq!(test_save[obj_offset_end], 88);
    assert_eq!(test_save[obj_offset_end + 1], 1);

    let static_offset_start = obj_offset_end + 2;
    let static_offset_end = 19631;

    let mut next_stat_start = static_offset_start;
    for i in static_offset_start..static_offset_end {
        if i == next_stat_start + 2 || i == next_stat_start + 3 {
            //visspot nulled out
            assert_eq!(test_save[i], 0, "visspot not nulled")
        } else {
            assert_eq!(
                orig_save[i], test_save[i],
                "statics bytes[{}] differ {:x} (orig) vs {:x} (test)",
                i, orig_save[i], test_save[i]
            );
        }

        if i == next_stat_start + (STAT_TYPE_LEN - 1) {
            next_stat_start += STAT_TYPE_LEN;
        }
    }

    // remaining savegame bytes should match exactly
    for i in static_offset_end..test_save.len() - 4 {
        assert_eq!(
            orig_save[i], test_save[i],
            "bytes[{}] differ {:x} (orig) vs {:x} (test)",
            i, orig_save[i], test_save[i]
        );
    }

    let checksum = i32::from_le_bytes(
        test_save[test_save.len() - 4..test_save.len()]
            .try_into()
            .expect("checksum"),
    );
    assert_eq!(checksum, 130078);
}

