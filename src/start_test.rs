// Helper

fn door(
    num: usize,
    tile_x: usize,
    tile_y: usize,
    vertical: bool,
    lock: usize,
    act: usize,
    tic: u32,
) -> DoorType {
    DoorType {
        num,
        tile_x,
        tile_y,
        vertical,
        lock: DoorLock::try_from(lock).expect("DoorLock"),
        action: DoorAction::try_from(act).expect("DoorAction"),
        tic_count: tic,
        position: 0,
    }
}

fn stat(tile_x: usize, tile_y: usize, sprite: usize, flags: u8, item_number: usize) -> StaticType {
    StaticType {
        tile_x,
        tile_y,
        sprite: must_sprite(sprite),
        flags,
        item_number: must_kind(item_number),
    }
}

fn must_sprite(num: usize) -> Sprite {
    Sprite::try_from(num).expect("valid Sprite for testing")
}

fn must_kind(num: usize) -> StaticKind {
    StaticKind::try_from(num).expect("valid SpriteKind for testing")
}

fn actor(
    active: i16,
    tic_count: u32,
    class: usize,
    flags: u8,
    dist: i32,
    dir: usize,
    x: i32,
    y: i32,
    tile_x: usize,
    tile_y: usize,
    view_x: i32,
    view_height: i32,
    trans_x: i32,
    trans_y: i32,
    area_num: usize,
    angle: i32,
    hitpoints: i32,
    speed: i32,
    temp1: i32,
    temp2: i32,
    temp3: i32,
) -> ObjType {
    let mut actor = null_obj_type();
    actor.active = ActiveType::try_from(active).expect("ActiveType");
    actor.tic_count = tic_count;
    actor.class = ClassType::try_from(class).expect("ClassType");
    actor.flags = flags;
    actor.distance = dist;
    actor.dir = DirType::try_from(dir).expect("DirType");
    actor.x = x;
    actor.y = y;
    actor.tilex = tile_x;
    actor.tiley = tile_y;
    actor.view_x = view_x;
    actor.view_height = view_height;
    actor.trans_x = new_fixed_i32(trans_x);
    actor.trans_y = new_fixed_i32(trans_y);
    actor.area_number = area_num;
    actor.angle = angle;
    actor.hitpoints = hitpoints;
    actor.speed = speed;
    actor.temp1 = temp1;
    actor.temp2 = temp2;
    actor.temp3 = temp3;
    actor
}

