use super::assets::{carmack_expand};

#[test]
pub fn test_carmack_expand() {
	let compressed = [0x00, 0xA7, 0x12, 0xEE, 0xFF, 0x00, 0xA8, 0x34, 0xCC, 0xDD];
	let decompressed = [0x12, 0xA7, 0xEE, 0xFF, 0x34, 0xA8, 0xCC, 0xDD];
	
	let result = carmack_expand(&compressed, decompressed.len());
	
	assert_eq!(result, decompressed);
}